package DecimalFractiomalNumber;


public final class DecimalFractionalNumber {
    private final String hole;
    private final String fractional;

    public DecimalFractionalNumber(String hole, String fractional){
    this.hole = hole;
    this.fractional = fractional;
    }

    public String getHole(){
        return hole;
    }
    public String getFractional(){
        return fractional;
    }

}
